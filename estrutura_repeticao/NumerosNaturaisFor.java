
public class NumerosNaturaisFor {
    public static void main(String[] args) {
        int i;
        int vetorNaturais[] = new int[101];       
        for (i = 0; i < vetorNaturais.length; i++) {
            vetorNaturais[i] = i;
            System.out.print(vetorNaturais[i] + " ");
        }
    }
}
