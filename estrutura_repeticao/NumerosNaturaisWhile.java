public class NumerosNaturaisWhile {
    public static void main(String[] args) {
        int numerosNaturais = 0;
        System.out.println("\nNumeros naturais entre 0 e 100:\n");       
        while (numerosNaturais <= 100) {    
        System.out.print(numerosNaturais + " ");  
        numerosNaturais++;  
        }
    }
}
